
// for loop
val items = listOf("apple", "banana", "kiwifruit")
for (item in items) {
    println(item)
}
/*
apple
banana
kiwifruit
*/

// for loop index
val items = listOf("apple", "banana", "kiwifruit")
for (index in items.indices) {
    println("item at $index is ${items[index]}")
}
/*
item at 0 is apple
item at 1 is banana
item at 2 is kiwifruit
*/




